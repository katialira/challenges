from left_rotation import rotLeft

def test_rot_Left():
	t1_a = [1, 2, 3, 4, 5]
	t1_n = 4
	assert rotLeft(t1_a, t1_n) == [5, 1, 2, 3, 4]

	t2_a = [41, 73, 89, 7, 10, 1, 59, 58, 84, 77, 77, 97, 58, 1, 86, 58, 26, 10, 86, 51]
	t2_n = 10
	assert rotLeft(t2_a, t2_n) == [77, 97, 58, 1, 86, 58, 26, 10, 86, 51, 41, 73, 89, 7, 10, 1, 59, 58, 84, 77]

	t3_a = [98, 67, 35, 1, 74, 79, 7, 26, 54, 63, 24, 17, 32, 81]
	t3_n = 7
	assert rotLeft(t3_a, t3_n) == [26, 54, 63, 24, 17, 32, 81, 98, 67, 35, 1, 74, 79, 7]
	
	t4_a = [431, 397, 149, 275, 556, 362, 852, 789, 601, 357, 516, 575, 670, 507, 127, 888, 284, 405, 806, 27, 495, 879, 976, 467, 342, 356, 908, 750, 769, 947, 425, 643, 754, 396, 653, 595, 108, 75, 347, 394, 935, 252, 683, 966, 553, 724, 629, 567, 93, 494, 693, 965, 328, 187, 728, 389, 70, 288, 509, 252, 449]
	t4_n = 48
	assert rotLeft(t4_a, t4_n) == [93, 494, 693, 965, 328, 187, 728, 389, 70, 288, 509, 252, 449, 431, 397, 149, 275, 556, 362, 852, 789, 601, 357, 516, 575, 670, 507, 127, 888, 284, 405, 806, 27, 495, 879, 976, 467, 342, 356, 908, 750, 769, 947, 425, 643, 754, 396, 653, 595, 108, 75, 347, 394, 935, 252, 683, 966, 553, 724, 629, 567]
	
	t5_a = [344, 210, 102]
	t5_n = 2
	assert rotLeft(t5_a, t5_n) == [102, 344, 210]
